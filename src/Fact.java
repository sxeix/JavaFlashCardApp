public class Fact {
    private String question;
    private String answer;

    public Fact() {}
    public String getQuestion() {
        return question;
    }
    public String getAnswer() {
        return answer;
    }
    public void setQuestion(String x) {
        this.question = x;
    }
    public void setAnswer(String x) {
        this.answer = x;
    }
}
